"""
2. sort() : arranges the element of the list in increasing order.

Let's see an example of sort() method.
"""
fav_fruits = ['apples','oranges','pears','bananas','pineapples']

# printing favorite fruits
print(fav_fruits)

# using sort to arrange the elements
fav_fruits.sort()

# printing the sorted list
print(fav_fruits)
