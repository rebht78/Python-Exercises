"""
4. remove() : is used to delete elements from the list, if you don't know
the index of the element

Let's see an example of remove() method.
"""
fav_fruits = ['apples','oranges','pears','bananas','pineapples']

# printing favorite fruits
print(fav_fruits)

# removing pears from fav_fruits list
fav_fruits.remove('pears')

# printing the sorted list
print(fav_fruits)
